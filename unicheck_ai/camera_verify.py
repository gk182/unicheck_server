threshold = face_logic.DEFAULT_THRESHOLD
margin = 0.04


def get_face_vector(frame):
    rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
    encoding, _ = face_logic._extract_robust_embedding(rgb, num_jitters=3)
    location, _ = face_logic._detect_single_face(rgb)

    if location is None or encoding is None:
        return None, [] if location is None else [location]

    return encoding, [location]


def compare_face(unknown_vector):
    known_vectors = face_logic._normalize_known_vectors(known_vector)
    distance = np.min(face_logic.face_recognition.face_distance(
        known_vectors,
        np.asarray(unknown_vector, dtype=np.float64)
    ))

    is_match = distance <= (threshold + margin)
    confidence = face_logic._distance_to_confidence(distance, threshold + margin)

    return is_match, confidence


cap = cv2.VideoCapture(0)

print("Camera started... press Q to quit")

while True:

    ret, frame = cap.read()

    if not ret:
        break

    vector, locations = get_face_vector(frame)

    text = "No face"

    if vector is not None:

        is_match, confidence = compare_face(vector)

        if is_match:
            text = f"MATCH {confidence}%"
            color = (0, 255, 0)
        else:
            text = f"NOT MATCH {confidence}%"
            color = (0, 0, 255)

    else:
        color = (255, 255, 0)

    for (top, right, bottom, left) in locations:

        cv2.rectangle(
            frame,
            (left, top),
            (right, bottom),
            color,
            2
        )

        cv2.putText(
            frame,
            text,
            (left, top - 10),
            cv2.FONT_HERSHEY_SIMPLEX,
            0.7,
            color,
            2
        )

    cv2.imshow("Face Verify Camera", frame)

    if cv2.waitKey(1) & 0xFF == ord('q'):
        break


cap.release()
cv2.destroyAllWindows()
